function showNav() {
    var x = document.getElementById("quickjumpmenuwithheader");
    if (x.style.display === "block") {
      x.style.display = "none";
    } else {
      x.style.display = "block";
    }
}

function showMenu() {
    var x = document.getElementById("homemenuwithheader");
    if (x.style.display === "block") {
      x.style.display = "none";
    } else {
      x.style.display = "block";
    }
}